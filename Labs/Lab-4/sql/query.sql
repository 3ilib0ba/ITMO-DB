INNER JOIN "Н_ВЕДОМОСТИ" НВ on ТВ."ИД" = НВ."ТВ_ИД"
WHERE ТВ."ИД" = 3 AND НВ."ИД" < 1250981 AND НВ."ИД" = 39921
