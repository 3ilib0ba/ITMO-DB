-- Сделать запрос для получения атрибутов из указанных таблиц, применив фильтры по указанным условиям:
-- Таблицы: Н_ЛЮДИ, Н_ОБУЧЕНИЯ, Н_УЧЕНИКИ.
-- Вывести атрибуты: Н_ЛЮДИ.ИМЯ, Н_ОБУЧЕНИЯ.ЧЛВК_ИД, Н_УЧЕНИКИ.ИД.
-- Фильтры: (AND)
-- a) Н_ЛЮДИ.ИМЯ = Роман.
-- b) Н_ОБУЧЕНИЯ.НЗК = 933232.
-- Вид соединения: RIGHT JOIN.

explain analyse select ЛЮДИ."ИМЯ", ОБ."ЧЛВК_ИД", УЧ."ИД" from "Н_ЛЮДИ" ЛЮДИ
RIGHT JOIN "Н_ОБУЧЕНИЯ" ОБ on ЛЮДИ."ИД" = ОБ."ЧЛВК_ИД"
RIGHT JOIN "Н_УЧЕНИКИ" УЧ on УЧ."ЧЛВК_ИД" = ЛЮДИ."ИД"
WHERE "ИМЯ" = 'Роман' AND ОБ."НЗК" = '933232'; -- Результатов не нашлось, поэтому я сделал фильтрацию отдельной

select ЛЮДИ."ИМЯ", ОБ."ЧЛВК_ИД", УЧ."ИД" from "Н_ЛЮДИ" ЛЮДИ
RIGHT JOIN "Н_ОБУЧЕНИЯ" ОБ on ЛЮДИ."ИД" = ОБ."ЧЛВК_ИД"
RIGHT JOIN "Н_УЧЕНИКИ" УЧ on УЧ."ЧЛВК_ИД" = ЛЮДИ."ИД"
WHERE ОБ."НЗК" = '933232';

select ЛЮДИ."ИМЯ", ОБ."ЧЛВК_ИД", УЧ."ИД" from "Н_ЛЮДИ" ЛЮДИ
RIGHT JOIN "Н_ОБУЧЕНИЯ" ОБ on ЛЮДИ."ИД" = ОБ."ЧЛВК_ИД"
